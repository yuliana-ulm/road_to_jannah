            <p>
                <b>Tanya :</b> Niat apa nang dimaksud dalam baair wan mandi(wajib) ? Apa hukum gawian nang digawi kada baniat wan apa dalilnya ?
            </p>
            <p>
                <b>Jawab :</b> nang dimaksud dalam baair wan mandi (wajib) adalah niat gasan mahilangakan hadats atawa manjadiakan bulih kadanya suatu parbuatan nang diwajibakan barsuci,
                makanya amalan-amalan nang digawi kada baniat kada pacang ditarima. Dalilnya adalah firman Allah, <i>"Dan mereka tidaklah diperintahkan melainkan agar beribadah kepada Allah dengan memurnikan
                ketaatan kepada-Nya dalam menjalankan agama dengan lurus"</i>(QS. Al-Bayyinah: 5)
            </p>
            <p>
                Dan Hadits dari Umar bin al-khattab, bahwa rasulullah basabda,
                <i>"sesungguhnya segala amalan itu tidak lain tergantung pada niat; dan sesungguhnya tiap-tiap orang tidak lain (akan memperoleh balasan dari) apa yang diniatkannya.
                Barangsiapa hijrahnya menuju (keridhaan) Allah dan rasul-Nya, maka hijrahnya itu ke arah (keridhaan) Allah dan rasul-Nya.
                Barangsiapa hijrahnya karena (harta atau kemegahan) dunia yang dia harapkan, atau karena seorang wanita yang ingin dinikahinya, maka hijrahnya itu ke arah yang ditujunya."</i>
            </p>
            <p>
                <b>Tanya :</b> Apa nitu wudhu atawa baair ? Apa dalil nang mamadahakan wudhu atawa baair tu wajib ? Wan apa nang mawajib akan kita wudhu atawa baair ?
            </p>
            <p>
                <b>Jawab :</b>Nang dimaksud wudhu atau baair adalah mamakai banyu nang suci wan mansuciakan wan jua mamakai cara nang khusus di ampat angguta badan yaitu, muha, kadua tangan, kapala wan kadua batis.
                Sabab nang mawajibakan kita baair adalah hadats, yaitu apa haja nang mawajibakan baair atawa mandi [tabagi jadi dua macam, (Hadats besar),yaitu apa haja nang mawajibakan mandi
                dan (Hadats kecil) yaitu apa haja nang mawajibakan baair].
                Dalil nang mawajibakan baair adalah firman Allah, <i>"Hai orang-orang yang beriman,
                apabila kamu hendak mengerjakan shalat, maka basuhlah mukamu dan tanganmu sampai dengan siku, dan sapulah kepalamu dan (basuh) kakimu sampai dengan kedua mata kaki. (QS. Al-Maidah: 6)"</i>
            </p>
            <p>
                <b>Tanya :</b> Apa dalil nang mawajibakan kita babismillah lamunnya handak baair wan gugur nya kawajiban lamunnya kada ingat atawa kada tahu ?
            </p>
            <p>
                <b>Jawab :</b> Dalil nang mawajibakan kita babismillah sabalum baair adalah hadits nang diriwayatakan Abu Hurairah dari Nabi,
                beliau basabda <i>"Tidak sah shalat bagi orang yang tidak berwudhu dan tidak sah wudhu orang yang tidak menyebut nama Allah atas wudhunya."</i>
            </p>
            <p>
                Lamunnya dalil gugurnya kawajiban babismillah marga kada ingat atawa kada tahu adalah hadits,<i>"dimaafkan untuk umatku, kesalahan dan kelupaan."</i> Tempatnya adalah di lisan dengan
                cara babismillah
            </p>
            <p>
                <b>Tanya :</b> Apa haja syarat-syarat baair ?
            </p>
            <p>
                <b>Jawab :</b> Syarat sah baair adalah :
                (1). Islam, (2). Berakal, (3). Tamyiz (dapat membedakan antara baik dan buruk),
                (4). Niat, (5). Istishab hukum niat, (6). Tidak adanya yang mewajibkan wudhu, (7). Istinja dan istijmar sebelumnya (bila setelah buang hajat),
                (8). Air yang thahur (suci lagi mensucikan), (9). Air yang mubah (bukan hasil curian -misalnya-),
                (10). Menghilangkan sesuatu yang menghalangi air meresap dalam pori-pori.
            </p>
            <p>
                <b>Tanya :</b> Ada barapa rukun wudhu atawa baair ?
            </p>
            <p>
                <b>Jawab :</b> Rukun wudhu atau baair ada 6,yaitu :
                <ol>
                    <li>Mambasuh muha(tarmasuk bakukumur wan mamasukkan banyu kahidung imbah tu dikalurakan)</li>
                    <li>Mambasuh tangan hinggan siku</li>
                    <li>Mambasuh kapala (tarmasuk mangusap daun talinga)</li>
                    <li>Mambasuh batis hinggan buku lali</li>
                    <li>Baurutan</li>
                    <li>Muwalah(kada tapagat lawan parkara diluar wudhu atawa baair)</li>
                </ol>
            </p>
            <p>
                <b>Tanya :</b>Hinggan mana kita Mambasuh muha
            </p>
            <p>
                <b>Jawab :</b>Mambasuh muha adalah mulai dari tampat tumbuhnya rambut kapala nang normal hinggan janggut nang turun dari jabis wan dagu, wan dari talinga kanan hinggan
                talinga kiwa malibar. Wajib mambasuh sabarataan bagian muha bagi nang kada labat janggutnya lawan kulit nang ada disala-sala janggut.
            </p>
            <p>
                <b>Tanya :</b>Apa nang dimaksud lawan baurutan
            </p>
            <p>
                <b>Jawab :</b>Nang dimaksud baurutan tu adalah sabagaimana nang ada didalam Al-Qur'an yaitu mambasuh muha, mambasuh kadua tangan(hinggan siku), imbah tu mambasuh kapala,
                hanyar mambasuh batis.
            </p>
            <p>
                Dalilnya adalah Al-Maidah ayat 6
            </p>
            <p>
                Kedua sabda rasulullah, "Mulailah dengan apa yang Allah telah memulai dengannya"
            </p>
            <p>
                Ketiga,hadits nang diriwayatkan dari Amr' bin 'Abasah. Sidin baucap, <i>"kalian mendekati air wudhunya, kemudian berkumur-kumur,
                memasukkan air ke hidungnya lalu mengeluarkannya kembali, melainkan gugurlah dosa-dosa di (rongga) mulut dan rongga hidungnya bersama air wudhunya,
                kemudian (tidaklah) ia membasuh mukanya sebagaimana yang Allah perintahkan,
                melainkan gugurlah dosa-dosa wajahnya melalui ujung-ujung janggutnya bersama tetesan air wudhu, kemudian (tidaklah) ia membasuh kedua tangannya sampai ke siku,
                melainkan gugurlah dasa-dosa tangannya bersama air wudhu melalui jari-jari tangannya, kemudian (tidaklah) ia mengusap kepalanya,
                melainkan gugur dosa-dasa kepalanya bersama air melalui ujung-ujung rambutnya, kemudian (tidaklah) ia membasuh kedua kakinya,
                melainkan gugur dosa-dasa kakinya bersama air melalui ujung-ujung jari kakinya."</i>
            </p>
            <p>
                <b>Tanya :</b>Apa nang dimaksud lawan muwalah
            </p>
            <p>
                <b>Jawab :</b>Maksudnya adalah jangan maakhirakan mambasuh anggota wudhu sampai anggota sebelumnya karing
            </p>
            <p>
                Dalilnya, hadits riwayat Ahmad dan Abu Dawud dari Nabi, bahwa sidin suah maliat lakian nang dibatisnya ada sebagian besar mata uang logam nang kada kana banyu,
                maka sidin manyuruh maulangi wudhunya
            </p>
            <p>
                Imam Ahmad mariwayatakan dari Umar bin al-khattab bahwa ada lakian nang berwudhu, tagal maninggal akan satu bagian nang ganalnya kaya kuku dibatisnya (kada kana banyu wudhu).
                Rasulullah malihat dan baucap, <i>"berwudhulah kembali, kemudian sholatlah."</i> Amunnya dalam riwayat Muslim kadada lafal, <i>"berwudhulah kembali."</i>
            </p>
            <p>
                <b>Tanya :</b>Kiyapa cara berwudhu atau baair yang sempurna
            </p>
            <p>
                <b>Jawab :</b>Nang panambayan,kita harus baniat,imbah itu babismillah wan mambasuh tangan 3 kali, imbah itu bakukumur wan mamasukakan banyu kahidung sabanyak 3 kali.
                Imbah itu membasuh muha 3 kali , imbah tu mambasuh kadua tangan hinggan siku sabanyak 3 kali, imbah ngitu mangusap kapala 1 kali mulai tempat tumbuh rambut bagian muka
                hinggan tempat tumbuh rambut di batang gulu bagian balakang. Imbah itu bulik akan usapan tadi ka tempat awal, imbah tu masuk akan jari talunjuk ka talinga wan usap daun talinga
                mamakai ibu jari. Imbah itu basuh kadua batis hinggan buku lali sabanyak 3 kali.
            </p>
            <p>
                <b>Tanya :</b>Apa dalil dari tata cara baair nang sampurna
            </p>
            <p>
                <b>Jawab :</b>Riwayat Abdullah bin Zaid tentang tatacara wudhu (terdapat lafal),
                <i>"Kemudian Rasulullah memasukkan tangannya, kemudian berkumur dan memasukkan air ke dalam hidung dengan satu tangan sebanyak tiga kali." </i>(Mutafaq 'alaih)
            </p>
            <p>
                <i>"Dan dari Humran bahwa Utsman pernah meminta dibawakan air wudhu, maka ia membasuh kedua telapak tangannya tiga kali,
                kemudian membasuh tangan kanannya sampai ke siku tiga kali, kemudian tangan kirinya seperti itu pula,
                kemudian mengusap kepalanya, kemudian membasuh kaki kanannya sampai mata kaki tiga kali, kemudian kaki kirinya seperti itu pula, kemudian berkata,
                'Aku melihat Rasulullah berwudhu seperti wudhuku ini.'"</i> (Mutafaq alaih)
            </p>
            <p>
                Dan dari Abdullah bin Zaid bin Ashim dalam tatacara wudhu, ia berkata, <i>"Dan Rasulullah mengusap kepalanya, menyapukannya ke belakang dan ke depan."</i> (Mutafaq alaih)
            </p>
            <p>
                Dan lafal yang lain, <i>"(Beliau) memulai dari bagian depan kepalanya sampai ke tengkuk, kemudian menariknya lagi ke bagian depan tempat semula memulai."</i>
            </p>
            <p>
                Dan dalam riwayat Ibnu Amr tentang tata cara berwudhu, katanya,
                <i>"Kemudian (Rasulullah) mengusap kepalanya, dan memasukkan dua jari telunjuknya ke masing-masing telinganya,
                dan mengusapkan kedua jari jempolnya ke permukaan daun telinganya."</i> (HR. Abu Dawud, Nasa'i dan disahihkan oleh Ibnu Khuzaimah)
            </p>
            <p>
                <b>Tanya :</b>Apa haja nang tarmasuk sunnah baair
            </p>
            <p>
                <b>Jawab :</b> Nang tamasuk sunnah baair adalah :
                <ol>
                    <li>Menyempurna akan wudhu</li>
                    <li>Mambasuh sala sala jari</li>
                    <li>Malabihakan mamasukakan banyu ka dalam hidung kacuali nang puasa</li>
                    <li>Mandahulu akan nang kanan</li>
                    <li>Basiwak</li>
                    <li>Mambasuh talapak tangan 3 kali</li>

                </ol>
            </p>
            <p>
                <b>Tanya :</b>Barapa takaran banyu  gasan baair wan mandi
            </p>
            <p>
                Takaran banyu gasan baair adalah satu mud (satu mud sama lawan 11/3 liter manurut ukuran urang hijaz wan 2 liter manurut urang irak. Lihat Lisanul Arab jilid 3 halaman 400).
                Lamunnya gasan mandi sabanyak satu sha' sampai 5 mud.
            </p>
            <p>
                <b>Tanya :</b>Bacaan apa nang dibaca imbah baair
            </p>
            <p>
                <i>"Berkata Rasulullah, 'Tidaklah salah seorang diantara kalian berwudhu dan menyempurnakan wudhunya,
                kemudian mengucapkan: asyhadu anlaa ilaaha illalloohu wahdahu laa syariikalahu wa asyhadu anna Muhammadan
                'abduhu wa Rosuuluh (Aku bersaksi bahwa tidak ada Ilah yang berhak disembah selain Allah semata; yang tidak ada sekutu baginya.
                Dan aku bersaksi bahwa Muhammad adalah hamba-Nya dan utusan-Nya), melainkan dibukakan untuknya delapan pintu syurga, ia dapat masuk dari mana saja yang ia kehendaki.'"" (HR. Muslim)"</i>
            </p>
