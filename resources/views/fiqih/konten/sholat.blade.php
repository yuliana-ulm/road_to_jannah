            1. Penghapus Dosa
            Di antara keutamaan shalat jum'at adalah sebagai penghapus dosa antara dua Jumat. Hadits dari Abu Hurairah RA yang:<br><br>

            الصَّلاَةُ الْخَمْسُ وَالْجُمُعَةُ إِلَى الْجُمُعَةِ كَفَّارَةٌ لِمَا بَيْنَهُنَّ مَا لَمْ تُغْشَ الْكَبَائِرُ

            <br><br>“Sholat lima waktu dan Jumat yang satu ke Jumat yang berikutnya dapat menghapuskan dosa di antara keduanya selama tidak dilakukan dosa besar.”

            2. Pahala Besar
            Keutamaan shalat jumat lainnya adalah memperoleh pahala besar. Pahala ini senilai pahala qurban yang disesuaikan dengan waktu kedatangannya di masjid. Sebagaimana hadits yang diriwayatkan oleh Imam Bukhari dan Imam Muslim:<br><br>

            مَنْ اغْتَسَلَ يَوْمَ الْجُمُعَةِ غُسْلَ الْجَنَابَةِ ثُمَّ رَاحَ فَكَأَنَّمَا قَرَّبَ بَدَنَةً وَمَنْ رَاحَ فِي السَّاعَةِ الثَّانِيَةِ فَكَأَنَّمَا قَرَّبَ بَقَرَةً وَمَنْ رَاحَ فِي السَّاعَةِ الثَّالِثَةِ فَكَأَنَّمَا قَرَّبَ كَبْشًا أَقْرَنَ وَمَنْ رَاحَ فِي السَّاعَةِ الرَّابِعَةِ فَكَأَنَّمَا قَرَّبَ دَجَاجَةً وَمَنْ رَاحَ فِي السَّاعَةِ الْخَامِسَةِ فَكَأَنَّمَا قَرَّبَ بَيْضَةً فَإِذَا خَرَجَ الْإِمَامُ حَضَرَتْ الْمَلَائِكَةُ يَسْتَمِعُونَ الذِّكْرَ

            <br><br>"Barangsiapa mandi pada hari Jumat sebagaimana mandi jinabat, lalu berangkat menuju masjid, maka dia seolah berqurban dengan seekor unta. Barangsiapa yang datang pada saat kedua maka dia seolah berqurban dengan seekor sapi. Barangsiapa yang datang pada saat ketiga maka dia seolah berqurban dengan seekor kambing yang bertanduk. Barangsiapa yang datang pada saat keempat maka dia seolah berqurban dengan seekor ayam. Dan barangsiapa yang datang pada sat kelima maka dia seolah berqurban dengan sebutir telur. Dan apabila imam sudah keluar (menyampaikan khutbah), maka para malaikat hadir mendengarkan dzikir (khutbah tersebut)"

            3. Seperti Ibadah Setahun
            Keutamaan shalat jum'at yang sempurna yaitu yang diawali mandi, kemudian setiap langkahnya senilai puasa dan shalat setahun. Sebagaimana hadits yang diriwayatkan oleh Imam Tirmidzi:<br><br>

            مَنْ اغْتَسَلَ يَوْمَ الْجُمُعَةِ وَغَسَّلَ ، وَبَكَّرَ وَابْتَكَرَ ، وَدَنَا وَاسْتَمَعَ وَأَنْصَتَ ، كَانَ لَهُ بِكُلِّ خُطْوَةٍ يَخْطُوهَا أَجْرُ سَنَةٍ صِيَامُهَا وَقِيَامُهَا

            <br><br>"Barangsiapa yang mandi pada hari Jumat lalu ia bergegas pergi (ke masjid), mendapati khutbah mendengar dan memperhatikan, maka setiap langkah kakinya terhitung seperti puasa dan shalat setahun"
