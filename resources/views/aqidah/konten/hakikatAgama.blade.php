            <h2>Fatwa Syekh Muhammad bin Shalih Al-'Utsaimin</h2>
            <h2>Pertanyaan :</h2>
            <p><i>Fadhilatus syech</i>, apa garang Islam tu?</p>
            <h2>Jawaban :</h2>
            <p>
                Islam manurut makna nang umum adalah baibadah hanya kapada Allah lawan ibadah nang 
                disyariat akan ulih Rasulnya, sejak Allah disembah ulih hambanya awan syariatnya sampai kaina
                hari kiamat. Maka ulih marga nitu, (Islam amun memakai makna umum)mancakup syariat nang dibawa nabi Nuh
                'alaihis salam barupa hidayah wan kebenaran. Kaitu jua awan syariat nang dibawa nabi Ibrahim,imam nang hanif, sama jua
                awan syariat nang dibawa Nabi Musa wan Isa. Niya sasuai apa awan nang difirman akan Allah dalam ayat nang banyak nang mamadah akan bahwa
                syariat sebelumnya tu adalah basarah diri kapada Allah SWT.
            </p>
            <p>
                Tagal, lamunnya Islam manurut makna nang khusus yaitu syariat nang dibawa Nabi Muhammad. 
                Oleh marga nitu,syariat nang dibawa rasulullah mahapus sabarataan syariat nang tadahulu.
                Jadi siapa haja nang maumpati syariat nabi Muhammad maka inya tu muslim, wan siapa haja nang kada
                maumpati syariat nabi Muhammad maka inya tu lain muslim, marga inya kada basarah diri kapada Allah SWT,tagal
                maumpati hawa nafsunya. Oleh karena itu, Yahudi adalah kaum muslimin wayah jaman nabi Musa wan Nasrani adalah
                kaum muslim wayah jaman nabi Isa. Tagal, imbah diutusnya nabi Muhammad, bubuhannya tu maingkari nabi Muhammad,
                sahingga kada dianggap muslim lagi.
            </p>
            <p>
                Oleh karena itu, kada bulih bagi siapapun mayakini bahwasanya agama yahudi wan nasrani wayah ini 
                adalah agama nang bujur dan diterima di sisi Allah sama nang kaya Islam. Bahkan, siapa pun nang mayakini
                nang kaitu, maka inya tu kafir, kaluar pada Islam. Marga Allah bafirman,
                <p>الدِّينَ عِندَ اللّهِ الإِسْلاَمُ
                “Sesungguhnya agama (yang diridai) di sisi Allah hanyalah Islam.” (QS. Ali ‘Imran: 19)</p>
                
                <p>Allah Ta’ala juga berfirman,
                وَمَن يَبْتَغِ غَيْرَ الإِسْلاَمِ دِيناً فَلَن يُقْبَلَ مِنْهُ وَهُوَ فِي الآخِرَةِ مِنَ الْخَاسِرِينَ               
                “Barangsiapa mencari agama selain agama Islam, maka sekali-kali tidaklah akan diterima (agama itu) daripadanya, dan dia di akhirat termasuk orang-orang yang merugi.” (QS. Ali ‘Imran: 85)</p>
                
                <p>Inilah Islam yang diisyaratkan oleh Allah Ta’ala, yaitu Islam yang Allah Ta’ala berikan nikmat Islam tersebut kepada Nabi Muhammad dan umatnya sebagaimana firman Allah Ta’ala,
                الْيَوْمَ أَكْمَلْتُ لَكُمْ دِينَكُمْ وَأَتْمَمْتُ عَلَيْكُمْ نِعْمَتِي وَرَضِيتُ لَكُمُ الإِسْلاَمَ دِيناً 
                “Pada hari ini telah Aku sempurnakan untuk kamu agamamu, dan telah Aku cukupkan kepadamu nikmat-Ku, dan telah Aku ridai Islam itu menjadi agama bagimu.” (QS. Al-Maidah: 3)</p>

                dalil ngini jelas banar sudah bahwa salain umat ngini imbah diutusnya nabi Muhammad, buhannya tu lain Islam.
                Maka marga nitu, siapa haja nang mamilih salain Islam, maka agamanya tu kada ditarima wan pacang kada kawa mambari manfaat
                kaina di hari kiamat. Kada halal gasan kita maulah ungkapan bahwa agama salain Islam tu agama nang lurus. Marga nitu,
                salah banar bagi siapa haja nang manyambat agama yahudi wan nasrani adalah dangsanak kita, atawa manyambat agama buhannya tu 
                masih diakui.

                lamun kita sambat bahwa Islam tu adalah baibadah hanya kapada Allah sasuai lawan syariat nya, maka itu tamasuk jua 
                basarah diri kapada Allah sacara lahir atawa batin. Sahingga mancakup jua agama saluruhnya, akidah, amal gawian wan jua ucapan. Lamun nya Islam tu 
                disebutakan lawan iman , maka tamasuk jua amal lahiriyah , baik itu ucapan atawa parbuatan anggota badan. Amunnya iman maknanya amal batin, 
                barupa akidah atawa kayakinan hati. Perbedaan ini ditunjuk akan ulih firman Allah SWT
                <p>قَالَتِ الْأَعْرَابُ آمَنَّا قُل لَّمْ تُؤْمِنُوا وَلَكِن قُولُوا أَسْلَمْنَا وَلَمَّا يَدْخُلِ الْإِيمَانُ فِي قُلُوبِكُمْ
                “Orang-orang Arab Badui itu berkata, ‘Kami telah beriman.’ Katakanlah, ‘Kamu belum beriman, tapi katakanlah, ‘kami telah tunduk’, karena iman itu belum masuk ke dalam hatimu.’” (QS. Al-Hujurat: 14)</p>
                <p>Juga firman Allah Ta’ala berkaitan dengan kisah kaum Luth,
                فَأَخْرَجْنَا مَن كَانَ فِيهَا مِنَ الْمُؤْمِنِينَ فَمَا وَجَدْنَا فِيهَا غَيْرَ بَيْتٍ مِّنَ الْمُسْلِمِينَ
                “Lalu Kami keluarkan orang-orang yang beriman (mukmin) yang berada di negeri kaum Luth itu. Dan Kami tidak mendapati negeri itu, kecuali sebuah rumah dari orang yang berserah diri (muslim).” (QS. Az-Zariyat: 35-36)
                </p>
                Maka didalam ayat tu,mambeda akan antara mukmin wan muslim. Hal ini marga rumah nang ada dikampung tu adalah rumah Islam sacara lahiriyah, marga bini Nabi Luth nang bakhianat wan kafir tamasuk jua.
                Adapun nang kaluar pada rumah tu wan salamat, itu pang kaum muslim nang sabujurnya nang imannya tu sudah masuk kadalam hati.
                <br>
                <p>Perbedaan iman dan Islam ketika disebutkan bersamaan juga ditunjukkan oleh hadis dari sahabat Umar bin Al-Khattab radhiyallahu ‘anhu. Di dalam hadis tersebut, Jibril ‘alaihis salaam bertanya kepada Nabi shallallahu ‘alaihi wasallam tentang Islam dan iman. Maka, Nabi shallallahu ‘alaihi wasallam bersabda,
                ألإِسِلاَمُ أَنْ تَشْهَدَ أَنْ لاَ إِلَهَ إِلاَّ اللهُ وَأَنَّ مُحَمَّدًا رَسُوْلُ اللهِ وَتُقِيْمَ الصَّلاَةَ وَتُؤْتِيَ الزَّكاَةَ وَتَصُوْمَ رَمَضَانَ وَتَحُجَّ الْبَيْتَ إِنِ اسْتَطَعْتَ إِلَيْهِ سَبِيْلاً
                “Islam adalah kamu bersaksi bahwa tidak ada Tuhan yang berhak disembah selain Allah, dan bahwa Muhammad adalah utusan Allah, kamu mendirikan salat, menunaikan zakat, puasa Ramadan, dan pergi haji jika mampu.“
                Dan Nabi shallallahu ‘alaihi wasallam berkata tentang iman,
                أَنْ تُؤْمِنَ بِاللهِ وَمَلاَئِكَتِهِ وَكُتُبِهِ وَرُسُلِهِ وَالْيَوْمِ الآخِرِ وَتُؤْمِنَ بِالْقَدَرِ خَيْرِهِ وَشَرِّهِ  
                “Kamu beriman kepada Allah, malaikat-malaikat-Nya, kitab-kitab-Nya, rasul-rasul-Nya dan hari akhir, dan kamu beriman kepada qadar yang baik maupun yang buruk.”</p>
                <br>
                Kasimpulannya, amun Islam disebutakan secara mutlak(kdd keterangan nang lain), maka Islam tu mancakup kasaluruhan agama, sahingga iman tarmasuk didalamnya. Tagal lamunnya, 
                disebutakan lawan iman, maka Islam dimaknai sabagai amal lahiriyah barupa ucapan lisan wan amal parbuatan, wan iman dimaknai sabagai amal batin, barupa kayakinan dalam hati wan amalan hati.
